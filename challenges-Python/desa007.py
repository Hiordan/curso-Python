#Programa que leia duas notas e mostre sua média
nota1 = int(input('Digite a primeira nota do estudante: '))
nota2 = int(input('Digite a segunda nota do estudante: '))
media = (nota1+nota2)/2
print('A média de um aluno que obteve a nota um igual a {} e a nota dois igual a {} é {}.'.format(nota1, nota2, media))
