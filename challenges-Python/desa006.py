#Programa que mostre o dobro, triplo e a raiz quadrada de um número lido do teclado
numero = int(input('Digite um número que mostrarei o dobro, o tríplo e a raiz quadrada dele: '))
print('O dobro do número {} é {}, o seu triplo é {} e sua raiz quadrada é {}.'.format(numero,numero*2,numero*3,numero**(1/2)))

