#Programa que leia o sálario de um colaborador calcule 15% de acrescimo sobre o preço original

salarioanterior = float(input('Qual o sálario atual do funcionário? R$ '))
novosalario = salarioanterior * 1.15
print('O salário anterior do colaborador era de R$ {} e o novo é de R$ {:.2f}.'.format(salarioanterior, novosalario))

